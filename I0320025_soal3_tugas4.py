# Berat bagasi dalam Kg
Max =float(50 * 0.45)     # 1 lbs = 0.45 Kg
print("Berat Maksimum bagasi:",Max,"Kg")

#Input berat bagasi dalam Kg
x =float(input("Berat bagasi pertama:"))       # berat lebih 110 Kg
y =float(input("Berat bagasi kedua:"))         # berat 49 Kg

# output
print('berat benda x: ',x,"Kg" +',', x<= Max)
print('berat benda y: ',y,"Kg"+',', y<= Max)